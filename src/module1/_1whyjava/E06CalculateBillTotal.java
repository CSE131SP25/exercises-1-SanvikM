package module1._1whyjava;

public class E06CalculateBillTotal {
	//Exercise 6: Write code that will compute and print the sum
	//of the following bills to be paid:
	
	//89.50
	//14.75
	//45.12
	//92.50
	public static void main(String[] args) {
		double bill1 = 89.50;
		double bill2 = 14.75;
		double bill3 = 45.12;
		double bill4 = 92.50;
		double billtotal = bill1 + bill2 + bill3 + bill4;
		System.out.println("Bill total: " + billtotal);
	}
}

